async function main() {
  console.log("Seeding equipment sub-types...");

  let created = 0;
  for (const [typeName, subs] of Object.entries(subTypes)) {
    const eqType = await prisma.equipmentType.findUnique({
      where: { name: typeName },
    });
    if (!eqType) {
      console.warn(`  Equipment type not found: ${typeName}`);
      continue;
    }

    for (const subName of subs) {
      await prisma.equipmentSubType.upsert({
        where: {
          name_equipmentTypeId: { name: subName, equipmentTypeId: eqType.id },
        },
        update: {},
        create: { name: subName, equipmentTypeId: eqType.id },
      });
      created++;
    }
  }

  const count = await prisma.equipmentSubType.count();
  console.log(`Done. Seeded ${created} sub-types. Total in DB: ${count}`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
